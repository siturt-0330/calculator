-- ============================================================
-- 既存 BBS スレッドのタイトルから "[v3] " プレフィックスを除去
-- ============================================================
-- 旧 seed_dummy_v3_bbs.sql がタイトル先頭に "[v3] " を付けて投入していたが、
-- ユーザーに見えるべきではないので削除する。
-- 再実行可能 (べき等)。
-- ============================================================

update public.bbs_threads
set title = regexp_replace(title, '^\[v3\]\s*', '')
where title like '[v3]%';
